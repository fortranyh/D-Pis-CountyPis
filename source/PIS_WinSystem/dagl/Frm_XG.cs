        //查询
        private void buttonX1_Click(object sender, EventArgs e)
        {
            if (dtStart.Value > dtEnd.Value)
            {
                Frm_TJInfo("时间范围错误！", "起始时间必须小于终止时间！");
                return;
            }
            StringBuilder sb = new StringBuilder();

            if (!Txtblh.Text.Trim().Equals(""))
            {
                sb.AppendFormat("  study_no = '{0}'", Txtblh.Text.Trim().Replace("'", "").ToUpper());
            }
            else
            {
                sb.AppendFormat("  create_dt>='{0} 00:00:00' and create_dt<='{1} 23:59:59'", dtStart.Value.ToString("yyyy-MM-dd"), dtEnd.Value.ToString("yyyy-MM-dd"));
            }
            DataTable dt = new DataTable();
            DBHelper.BLL.report_xg_advice ins = new DBHelper.BLL.report_xg_advice();
            dt = ins.GetdtAdvice(sb.ToString());
            if (dt != null && dt.Rows.Count > 0)
            {
                superGridControl1.PrimaryGrid.DataSource = dt;
                //激活列表
                superGridControl1.Select();
                superGridControl1.Focus();
            }
            else
            {
                superGridControl1.PrimaryGrid.DataSource = null;
                Frm_TJInfo("提示", "不存在满足当前条件的报告修改信息！");
            }
        }
